package com.privatecloud.users.dto;

public class VMDto {

	String vmName;
	String vmState;
	String vmConn;
	
	public String getVmName() {
		return vmName;
	}
	public void setVmName(String vmName) {
		this.vmName = vmName;
	}
	public String getVmState() {
		return vmState;
	}
	public void setVmState(String vmState) {
		this.vmState = vmState;
	}
	public String getVmConn() {
		return vmConn;
	}
	public void setVmConn(String vmConn) {
		this.vmConn = vmConn;
	}	
}
